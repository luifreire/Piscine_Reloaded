void	ft_putchar(char c);

void	ft_is_negative(int n)
{
	char	pos;
	char	neg;

	pos = 'P';
	neg = 'N';
	if (n >= 0)
	{
		ft_putchar(pos);
	}
	else
	{
		ft_putchar(neg);
	}
}
